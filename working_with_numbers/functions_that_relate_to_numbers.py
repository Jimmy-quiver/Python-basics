#math module
from math import * #(imports math functions)

#function that relate to numbers
#absolute
print(abs(-5))
#power
print(pow(3, 2))#3^2
#greater number
print(max(4, 10))
#lesser number
print(min(2, 4, 5))
#round off a number
print(round(5.4999))
#floor (going to grab the whole number{chopping the decimal points})
print(floor(9.7))
#ceil (round up whatever decimal)
print(ceil(3.2))
#square root
print(sqrt(4.2))
